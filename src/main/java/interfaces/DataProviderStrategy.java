package interfaces;

import java.util.List;

/**
 * Интерфейс для стратегий предоставления данных различных типов.
 * Реализует паттерн Стратегия для различных источников данных.
 * Позволяет легко добавлять новые способы получения данных.
 *
 * @param <T> тип данных, которые предоставляет провайдер
 */
public interface DataProviderStrategy<T> {

    /**
     * Основной метод получения данных из указанного источника.
     * Реализующие классы должны обрабатывать ошибки ввода-вывода и валидацию данных.
     *
     * @param length количество элементов для получения
     * @param dataType тип данных с которыми работает пользователь
     * @return список объектов типа T (может быть пустым, но не null)
     * @throws IllegalArgumentException если параметры невалидны
     */
    List<T> provideData(int length, String dataType);
}